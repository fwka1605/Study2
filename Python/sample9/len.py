# 文字列(文字数）の長さ
print("あいうえおabc")
print(len("あいうえおabc"))
print()

# リストの要素数
lst=["a",1,False]
print(lst)
print(len(lst))
print()

# 辞書の要素数
dict = {1:1,2:2,3:3}
print(dict)
print(len(dict))
print()

# タプル(変更不可)の要素数 
tpl=(1,2,3,4,5)
print(tpl)
print(len(tpl))
print()

# 集合(重複不可)の要素数
sets1={1,2,3,4,5}
sets2={1,1,2,2,3,3}
print(sets1)
print(len(sets1))
print()
print(sets2)
print(len(sets2))
print()
