#数値
print(123)
print(type(123))
print()

#文字
print("abc")
print(type("abc"))
print()

#リスト
print(["a","b","c"])
print(type(["a","b","c"]))
print()

#辞書
print({"a":"b","c":"d","e":"f"})
print(type({"a":"b","c":"d","e":"f"}))
print()

#タプル
print(('a','b','c'))
print(type(('a','b','c')))
print()

#集合
print(set('hoge'))
print(type(set('hoge')))
print()

